/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrderBottom = function (root) {
  if (root == null) return []
  let res = []
  const dfs = (node, index) => {
    if (node == null) return
    if (!res[index]) {
      res[index] = []
    }
    res[index].push(node.val)
    dfs(node.left, index + 1)
    dfs(node.right, index + 1)
  }
  dfs(root, 0)
  return res.reverse()
}
